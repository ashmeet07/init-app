def test_supported_starter_apps_render_valid_python(tmp_path):
    frameworks = [
        ("fastapi", "ui", "8000"),
        ("flask", "templates", "5000"),
        ("bottle", "templates", "8080"),
        ("sanic", "ui", "8000"),
        ("falcon", "ui", "8000"),
        ("tornado", "ui", "8888"),
        ("pyramid", "ui", "6543"),
        ("base", "ui", "8000"),
        ("hp_cli", "ui", "8000"),
        ("data_pipeline", "ui", "8000"),
        ("dbt_analytics", "ui", "8000"),
    ]

    for framework, ui_folder, port in frameworks:
        project_root = tmp_path / framework
        ctx = {
            "project_name": f"{framework}_demo",
            "framework": framework,
            "fw_name": framework,
            "build_strategy": "standard",
            "ui_folder": ui_folder,
            "port": port,
            "host": "0.0.0.0",
            "APP_NAME": "init-app",
            "APP_TAGLINE": "High-Performance System & Native Extension Generator",
            "version": "3.1.0",
            "DEFAULT_PORTS": {framework: port},
        }
        generator = Generator(project_root, ctx)
        generator._render_and_write("common/entry.py.tpl", "app.py")

        app_py = project_root / "app.py"
        compile(app_py.read_text(), str(app_py), "exec")
